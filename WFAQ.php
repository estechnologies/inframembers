	<center><h3>FAQs</h3></center>
	<p><b>1. Who we are & what we do?</b></p>

<p>- We are just coordinators between the companies which are looking for the Execution Team (like 

contractors, machinery, and material & manpower suppliers for their projects and “the Execution 

Team”. And also we provide the list of opportunities available to “the Execution Team”.</p><br>

<p><b>2. What is the difference between EPC Company and the Contractors?</b><p>

<p>- An EPC company here in our case is the organization which is having works/contracts with them and 

which is looking for the Execution Team<p>

<p>- Contractors are those who are willing to take-up the tasks of the EPC companies</P><br>

<p><b>3. What is the difference between registration as “EPC Company” & as a “Contractor/Supplier”?</b></p>

<p>-  If you register as “EPC Company”, you can post the works and a list of interested Teams which are 

ready to work for you will be sent to you and you can choose from them</p>

<p>- If you register as “Contractor/Supplier”, your identity and available resources will be viewed by the EPC 

companies and they will contact you for their requirements or based on your interest, we can send your 

details to the concerned companies who posted their work details.</p><br>

<p><b>4. Is it required to register to apply for a work?</b></p>

<p>-  Yes obviously, because we need to provide your details to the concerned EPC Companies and “More 

the details you provided.. More the chances for a company to know about you and More the 

chances to get you enlisted for the work……..”</p><br>

<p><b>5.  Do you verify the details of the registered users like “Contractors / Suppliers”?</b></p>

<p>-  Yes, as our prime motto is to provide genuine “Execution Teams” to the companies, we will collect all 

the necessary details like Incorporation Certificates, work experiences, machinery details (like RCs etc.), 

Identity Proofs etc. and get them enlisted into our site.</p><br>

<p><b>6. Will I get the contract for the work I applied?</b></p>

<p>- The right to shortlist/awarding the contract will depends solely on the Client (EPC Company). It 

depends on your work experience and quotation/negotiations that you had with company at the time 

when they contacted you.</p></br>

<p><b>7. What if, if I don’t have any incorporation certificates or company registration certificates etc. ? </b></p>

<p>-  No Problem, we consider your ID proofs for registration purpose and if you are interested, we will help 

you in registering your firm… </p><br>

<p><b>8. Are the works posted in your website genuine? </b></p>

<p>-  Absolutely YES, our business development team continuously strives and meets all our clients and 

posts only the on-going works which are ready to be Sub-Contracted.</p><br>

<p><b>9. Why you are not showing the contact numbers of the EPC companies which posted the work?</b></p>

<p>-  Our clients are equally important as you are for us, in-order to prevent the mischievous calls from 

outside non-registered users and for the security reasons we are compelled to do it. Anyhow, as a 

registered member you can directly apply to the concerned work and we will provide your details to 

concerned company on your behalf and will following-up for you.</p><br>

 <p></b>10. Is personal/professional details provided to you are secure?</b></p>

<p>-  Yes, we never share your details to any third party; we only provide your details to the EPC Companies 

to whom you have applied for the work, so as to enable them to know about your experience.</p><br>

<p><b>11. What are the advantages for me if I post a work as an EPC Company?</b></p>
<p>-  Cost Saving …. Yes, cost saving.., you can post the work for free … You know that to post the same in 

news papers & all, it is chargeable…..  We will provide you with a list of experienced teams from which 

you the choose the one who is suitable for your work… you will have a greater bargaining power … </p><br>

<p><b>12. Why you are charging from us for registration?</b></p>

<p>-  To make the business run… Once you are registered with us, we keep following-up for the works you 

applied, we will inform you about the new works which are posted and also we assist you in developing 

your profile, so that the same can be used for your future purposes.</p>

</p>- Also, please check the registration charges… It is evident that our charges are very very less compared 

